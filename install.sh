#!/bin/sh

# install.sh – установка скрипта проверки WAN IP на OpenWRT с интерактивной настройкой
# Использование: curl -fsSL https://raw.githubusercontent.com/ВАШ_ЛОГИН/openwrt-wan-ip-check/main/install.sh | sh

set -e

REPO="Erridium/openwrt-wan-ip-check"
BRANCH="main"
BASE_URL="https://raw.githubusercontent.com/$REPO/$BRANCH"

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

echo "${GREEN}Установка скрипта проверки WAN IP для OpenWRT${NC}"
echo "------------------------------------------------"

# Проверка наличия curl
if ! command -v curl >/dev/null 2>&1; then
    echo "${RED}curl не найден. Устанавливаем...${NC}"
    opkg update && opkg install curl
fi

# Скачиваем основной скрипт
echo "${YELLOW}Скачивание основного скрипта...${NC}"
curl -fsSL -o /usr/bin/check_wan_ip.sh "$BASE_URL/check_wan_ip.sh"
chmod +x /usr/bin/check_wan_ip.sh

# Интерактивный ввод параметров
echo ""
echo "${GREEN}Настройка параметров (Enter - оставить значение по умолчанию)${NC}"

# WAN interface
DEFAULT_WAN="wan"
read -p "Имя WAN интерфейса [$DEFAULT_WAN]: " WAN_INTERFACE
WAN_INTERFACE=${WAN_INTERFACE:-$DEFAULT_WAN}

# Target network
DEFAULT_TARGET="109.108.32.0/19"
read -p "Желаемая сеть (CIDR) [$DEFAULT_TARGET]: " TARGET_NETWORK
TARGET_NETWORK=${TARGET_NETWORK:-$DEFAULT_TARGET}

# Unwanted network (optional)
DEFAULT_UNWANTED="100.64.0.0/10"
read -p "Нежелательная сеть (CIDR) для логирования, Enter если не нужна [$DEFAULT_UNWANTED]: " UNWANTED_NETWORK
UNWANTED_NETWORK=${UNWANTED_NETWORK:-$DEFAULT_UNWANTED}
# Если пользователь ввел пустую строку, оставляем пустой
if [ -z "$UNWANTED_NETWORK" ]; then
    UNWANTED_NETWORK=""
fi

# Check interval
DEFAULT_INTERVAL=120
read -p "Интервал проверки (секунд) [$DEFAULT_INTERVAL]: " CHECK_INTERVAL
CHECK_INTERVAL=${CHECK_INTERVAL:-$DEFAULT_INTERVAL}

# Restart delay
DEFAULT_DELAY=60
read -p "Задержка после перезапуска интерфейса (секунд) [$DEFAULT_DELAY]: " RESTART_DELAY
RESTART_DELAY=${RESTART_DELAY:-$DEFAULT_DELAY}

# Log file
DEFAULT_LOG="/var/log/wan_ip_check.log"
read -p "Файл лога [$DEFAULT_LOG]: " LOG_FILE
LOG_FILE=${LOG_FILE:-$DEFAULT_LOG}

# Создание конфигурационного файла
CONFIG_FILE="/etc/wan-ip-check.conf"
echo "${YELLOW}Создание конфигурационного файла $CONFIG_FILE...${NC}"
cat > "$CONFIG_FILE" << EOF
# Configuration for WAN IP check script
# Generated by install.sh on $(date)

WAN_INTERFACE="$WAN_INTERFACE"
TARGET_NETWORK="$TARGET_NETWORK"
UNWANTED_NETWORK="$UNWANTED_NETWORK"
CHECK_INTERVAL="$CHECK_INTERVAL"
RESTART_DELAY="$RESTART_DELAY"
LOG_FILE="$LOG_FILE"
EOF

echo "${GREEN}Конфигурация сохранена.${NC}"

# Настройка автозапуска через rc.local
echo "${YELLOW}Настройка автозапуска через /etc/rc.local...${NC}"
# Удаляем предыдущие строки с вызовом скрипта
sed -i '/check_wan_ip.sh/d' /etc/rc.local
# Добавляем запуск перед exit 0
sed -i "/exit 0/i /usr/bin/check_wan_ip.sh \&" /etc/rc.local

# Запуск скрипта
echo "${YELLOW}Запуск скрипта...${NC}"
/usr/bin/check_wan_ip.sh &

echo "${GREEN}Установка завершена!${NC}"
echo "Логи можно смотреть командой: logread | grep wan-ip-check"
echo "Или tail -f $LOG_FILE"
echo "Для изменения параметров отредактируйте $CONFIG_FILE и перезапустите скрипт."
